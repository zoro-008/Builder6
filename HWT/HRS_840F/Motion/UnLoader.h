class CUnLoader : public CPartInterface
{
    public: //초기화 등등.
        //Constructor
        CUnLoader ();
        ~CUnLoader ();

        void Init(bool _bFront );
        void Close();


    public:

        //Vars.
        struct SStat {
            bool bWorkEnd ;
            bool bReqStop ;
        };    //sun Clear When LOT Open. and every 30Sec in autorun()

        enum EN_SEQ_CYCLE {
            scIdle       =  0 ,
            scSupply     =  1 ,
            scIn         =  2 ,
            scWork       =  3 ,


            MAX_SEQ_CYCLE
        };

        int        m_iCycleSttTime             ;
        AnsiString m_sCycleName[MAX_SEQ_CYCLE] ;
        int        m_iCycleTime[MAX_SEQ_CYCLE] ; //마지막 사이클 시간. ms
        void   InitCycleName(){
            m_sCycleName[scIdle  ]   = "Idle    " ;
            m_sCycleName[scSupply]   = "Supply  " ;
            m_sCycleName[scIn    ]   = "In      " ;
            m_sCycleName[scWork  ]   = "Work    " ;
        }
        void   InitCycleTime(){
            memset(&m_iCycleTime  , 0 , sizeof(int )*MAX_SEQ_CYCLE);
        }

        struct SStep {
            int          iHome    ;
            int          iToStart ;
            EN_SEQ_CYCLE iSeq     ;
            int          iCycle   ;
            int          iToStop  ;
        };

    protected:
        AnsiString m_sPartName  ;
        //Timer.
        CDelayTimer m_tmMain    ;
        CDelayTimer m_tmCycle   ;
        CDelayTimer m_tmHome    ;
        CDelayTimer m_tmToStop  ;
        CDelayTimer m_tmToStart ;
        CDelayTimer m_tmDelay   ;

        SStat  Stat ;
        SStep  Step , PreStep ;

        double m_dLastIdxPos ;

        String m_sCheckSafeMsg ;

        void   ResetTimer();

        double GetMotrPos (EN_MOTR_ID _iMotr , EN_PSTN_ID _iPstnId );

        //똑같은 놈이 2개라 인덱스를 가지고 있는다.
        struct SAdd{
            EN_PART_ID     iPartId               ;
            EN_ARAY_ID     riULD                 ;
            EN_ARAY_ID     riPCK                 ;

            EN_ACTR_ID     aiULD_FoldUpDn        ;           
            EN_ACTR_ID     aiULD_PickUpFwBw      ;           
            EN_ACTR_ID     aiULD_RotateFwBw      ;           
            EN_ACTR_ID     aiULD_PackOpenFwBw    ;
            EN_ACTR_ID     aiULD_PackSrtFwBw     ;

            EN_INPUT_ID    xULD_MaskDetect       ;
            EN_INPUT_ID    xULD_PackVccm         ;
            EN_INPUT_ID    xULD_PickVccm         ;

            EN_OUTPUT_ID   yULD_ConMotorCw       ;
            EN_OUTPUT_ID   yULD_PickUpVccm       ;
            EN_OUTPUT_ID   yULD_PickUpEjet       ;
            EN_OUTPUT_ID   yULD_PackOpenVccm     ;
            EN_OUTPUT_ID   yULD_PackOpenEjet     ;

            bool bRunSkip ;

        };
        SAdd Add ;

    public: //인터페이스 상속.==================================================
	void Reset (); //리셑 버튼 눌렀을때 타는 함수.

        //Running Functions.
        bool ToStopCon (); //스탑을 하기 위한 조건을 보는 함수.
        bool ToStartCon(); //스타트를 하기 위한 조건을 보는 함수.
        bool ToStart   (); //스타트를 하기 위한 함수.
        bool ToStop    (); //스탑을 하기 위한 함수.

        int          GetHomeStep    (){return Step.iHome    ;} int GetPreHomeStep   (){return PreStep.iHome    ;}void InitHomeStep (){ Step.iHome  = 10 ; PreStep.iHome  = 0 ;}
        int          GetToStartStep (){return Step.iToStart ;} int GetPreToStartStep(){return PreStep.iToStart ;}
        int          GetSeqStep     (){return (int)Step.iSeq;} int GetPreSeqStep    (){return (int)PreStep.iSeq;}
        int          GetCycleStep   (){return Step.iCycle   ;} int GetPreCycleStep  (){return PreStep.iCycle   ;}void InitCycleStep(){ Step.iCycle = 10 ; PreStep.iCycle = 0 ;}
        int          GetToStopStep  (){return Step.iToStop  ;} int GetPreToStopStep (){return PreStep.iToStop  ;}

        AnsiString   GetCycleName   (int _iSeq){return m_sCycleName[_iSeq];}
        int          GetCycleTime   (int _iSeq){return m_iCycleTime[_iSeq];}
        AnsiString   GetPartName    (){return m_sPartName   ;}

        int          GetCycleMaxCnt (){return MAX_SEQ_CYCLE ;}

        bool         Autorun        (); //오토런닝시에 계속 타는 함수.
        //======================================================================
    public:
        bool   FindChip   (int &r , EN_ARAY_ID &id);

        //HomeCycle
        bool CycleHome(); //파트 홈을 잡을때 쓰는 함수.

        // Cycle.
        bool CycleIn     ();
        bool CycleWork   ();
        bool CycleSupply ();

        bool CheckSafe  (EN_MOTR_ID _iMotr , EN_PSTN_ID _iPstnId );
        bool CheckSafe  (EN_ACTR_ID _iActr , bool       _bFwd    );

        bool GetWorkEnd     (){return Stat.bWorkEnd ;}
        bool GetReqStop     (){return Stat.bReqStop ;}

        void  SetStat(SStat _sStat) { Stat = _sStat ; }
        SStat GetStat(            ) { return Stat;    }

        //Motor.
        //bool MoveMotr    (EN_MOTR_ID _iMotr , EN_PSTN_ID _iPstnId); // 모터를 움직일때 쓰는 함수.
        void MoveIncMotr (EN_MOTR_ID _iMotr , double _iPstn); // 모터를 움직일때 쓰는 함수.

        bool MoveMotr    (EN_MOTR_ID _iMotr , EN_PSTN_ID _iPstnId , bool _bSlow = false);
        
        //Actuator.
        bool MoveActr    (EN_ACTR_ID _iActr , bool _bFwd); //실린더를 움직일때 쓰는 함수.

        //For Start Stop Checking.
        void SetLastCmd (); //장비 정지시에 마지막 커맨드를 기억 하고 있다가.
        bool CheckMoved (); //이 함수를 이용해 움직였는지 확인 하고 장비 스타트 시에 그냥 작업 할 껀지 아니면 ToStart를 하고 할 건지 결정.
        bool CheckStop  (); //장비 정지시에 모든 모터 와 실린더 가 정지 되었는지 확인 하는 함수.

        //Loading Para.
        void Load(bool _bLoad);

        // Bin 관련
        int m_iBin1Cnt ; // Bin Clear 하기 위해 추가
        int m_iBin2Cnt ;

        int  GetBin1Cnt  () { return m_iBin1Cnt ; } ;
        int  GetBin2Cnt  () { return m_iBin2Cnt ; } ;

        void ClearBinCnt() {
            m_iBin1Cnt = 0;
            m_iBin2Cnt = 0;
        } ;
        
        void ClearBin1Cnt() { m_iBin1Cnt = 0; } ; // Bin 관련 추가
        void ClearBin2Cnt() { m_iBin2Cnt = 0; } ; // Bin 관련 추가

};

