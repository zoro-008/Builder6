    //          [0] : dwSelMon에서 선택한 감시 정보에 대하여 예외 처리 하지 않음.
    //         [>0] : dwSelMon에서 선택한 감시 정보에 대하여 예외 처리 기능 적용.
    // dwAction(0~3): dwActionValue 이상으로 감시 정보가 확인되었을때 예외처리 방법 설정.
    //          [0] : Warning(setting flag only) 
    //          [1] : Warning(setting flag) + Slow-down stop
